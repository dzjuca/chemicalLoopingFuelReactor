function f_s = freeboardSolidFractionFcn(u_g0_lp, G_sat, Global)
% -------------------------------------------------------------------------

    z2    = Global.reactor.z2;
    a_u0  = 7; % s-1
    rho_p = Global.carrier.bulkDensity;
    f_d   = 0.3;
% -------------------------------------------------------------------------

    a = a_u0./u_g0_lp;
    f_asterisk = G_sat./(u_g0_lp.*rho_p);

% -------------------------------------------------------------------------

    f_s = f_asterisk + (f_d - f_asterisk).*exp(-a.*z2);

% -------------------------------------------------------------------------
end